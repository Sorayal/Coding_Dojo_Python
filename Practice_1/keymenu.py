#!/usr/bin/python3

while True:
    key=input("Bitte geben Sie eine Auswahloption ein : W , B , N, Q für Quit \n")
    if key == "w":
        print("Sie haben Taste W gedrückt")
    elif key == "b":
        print("Sie haben Taste B gedrückt")
    elif key == "n":
        print("Sie haben Taste N gedrückt")
    elif key == "q":
        break
    else:
        print("Richtigen Buchstaben auswählen")
    print("\n\n\n")
    
    