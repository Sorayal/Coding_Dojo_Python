# Python is a dynamically interpreted language
# This means the datatype will be set at runtime 

#Text Type
# String
x = str("Universe") 

# Numeric Type
# Integer
x = int(20)

#float
x = float(8.5)

#complex number
x = complex(1j)


# Sequence Type
# list
x = list(("Car","Plane","Tank","Boat"))

# tuple
x = tuple(("Car","Airplane","Tank","Boat"))

# range
x = range(6)


# Set Types
# set
x = set(("Car","Airplane","Tank","Boat"))

# frozenset
x = frozenset(("Car","Airplane","Tank","Boat"))


# Mapping Type
# dict
x = dict(tankname = "Merkava", airplane = "Dornier")


# Boolean Type
# bool
x = bool(2)


# Binary Types
# bytes
x = bytes(128)

# bytearray
x = bytearray(10)

# memoryview
x = memoryview(bytes(8))